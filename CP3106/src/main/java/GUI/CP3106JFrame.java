    private void jButton6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton6MouseClicked
        // TODO add your handling code here:
        String fileString = jTextField5.getText();
        String Number = jTextField6.getText();
        int Num = Integer.parseInt(Number);
        String methodString = (String) jComboBox1.getSelectedItem();
        File temp = new File(fileString);
        if (methodString.equals("Student Track")) {
            //Student Track
            String[] fileStrings = {fileString};
            trace.rankStu(fileStrings);
            trace.getTrack(temp.getParent()+"\\ranked.csv");
            trace.MarkovMatrix();
            trace.collectStartPoints();
            trace.generateTrack(Num);
            trace.outputData(temp.getParent()+"\\new_"+temp.getName());
        }
        if (methodString.equals("Access Point")) {
            //Access Point
            String[] fileStrings = {fileString};
            Integer[] datasize = {Num};
            statistics.Collect_Generate(fileStrings, datasize);
        }
        jLabel12.setText("Path of output file: "+temp.getParent()+"\\new_"+temp.getName());
    }//GEN-LAST:event_jButton6MouseClicked

