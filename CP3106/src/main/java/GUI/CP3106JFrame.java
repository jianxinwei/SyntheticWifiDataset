    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        jTable2SelectedRow = jTable2.getSelectedRow();
        if (evt.getClickCount()==2) {
            jTable2.setValueAt(null, jTable2.getSelectedRow(), 1);
        }
        if(jTable2.getValueAt(jTable2.getSelectedRow(),0)!=null){
            DefaultTableModel model1 = (DefaultTableModel)jTable1.getModel();
            model1.setRowCount(0);
            fileString = jTextField4.getText()+"\\"+(String)jTable2.getValueAt(jTable2.getSelectedRow(),0);
            File selcetFile = new File(fileString);
            //            jTable1.getColumnModel().getColumn(0).setHeaderValue(fileString);

            try {
                BufferedReader reader_csv = new BufferedReader(new FileReader(selcetFile));
                String titlesString;
                String[] items;
                titlesString = reader_csv.readLine();
                items = titlesString.split(",");
                int delta_row = items.length - jTable1.getRowCount();
                Object[] new_row = new Object[jTable1.getColumnCount()];
                if( delta_row > 0 ){
                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                    for (int i = 0; i < delta_row; i++) {
                        model.addRow(new_row);
                    }
                }
                for (int i = 0; i < jTable1.getRowCount(); i++) {
                    jTable1.setValueAt(items[i], i, 0);
                }
                reader_csv.close();
            } catch (FileNotFoundException ex) {
                System.out.println("can not read file");
            } catch (IOException ex) {
                System.out.println("Error when read or write");
            }
        }
    }//GEN-LAST:event_jTable2MouseClicked

